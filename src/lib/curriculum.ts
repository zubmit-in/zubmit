/** List of all degree names */
export const degreeList: string[] = Object.keys(curriculum);

/**
 * Returns the list of specializations available for a given degree.
 * If the degree is not found, returns an empty array.
 */
export function getSpecializations(degree: string): string[] {
  const specs = curriculum[degree];
  return specs ? Object.keys(specs) : [];
}

/**
 * Returns the maximum semester number for a given degree.
 * Looks across all specializations and returns the highest semester number found.
 * If the degree is not found, returns 0.
 */
export function getMaxSemester(degree: string): number {
  const specs = curriculum[degree];
  if (!specs) return 0;

  let max = 0;
  for (const spec of Object.values(specs)) {
    for (const semStr of Object.keys(spec)) {
      const sem = Number(semStr);
      if (sem > max) max = sem;
    }
  }
  return max;
}

/**
 * Returns the list of subjects for a given degree, specialization, and semester.
 * If any lookup fails, returns an empty array.
 */
export function getSubjects(
  degree: string,
  specialization: string,
  semester: number
): string[] {
  const specs = curriculum[degree];
  if (!specs) return [];

  const semesters = specs[specialization];
  if (!semesters) return [];

  return semesters[semester] ?? [];
}
